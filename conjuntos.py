# Definimos dos conjuntos de nombres
mi_conjunto = {"Angel", "Sara", "Pilar"}
mi_conjunto2 = {"Angel", "Manolo", "Juan"}

# Calculamos la diferencia entre los conjuntos usando el operador -
diferencia = mi_conjunto - mi_conjunto2

# Imprimimos la diferencia
print(diferencia)
# {'Sara', 'Pilar'}

# Este código creará dos conjuntos, mi_conjunto y mi_conjunto2, con algunos nombres dentro de ellos. Luego, calcula la diferencia entre los dos conjuntos usando el operador -, que en este contexto da como resultado un conjunto que contiene los elementos que están en mi_conjunto pero no en mi_conjunto2. Finalmente, imprime esta diferencia, que en este caso serían los nombres "Sara" y "Pilar".


diferencia2 = mi_conjunto2 - mi_conjunto

print(diferencia2)
# {'Manolo', 'Juan'}






